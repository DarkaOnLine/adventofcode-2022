    protected function execute(InputInterface $input, OutputInterface $output)
    {
        $partOne = [];
        $partTwo = [];
        $startCoordinates = ['x' => 40, 'y' => 40];
        $headCoordinates = $startCoordinates;

        $knotsPool = [];
        $knotsAmount = 10;

        foreach ($this->inputData as $move) {
            for ($step = 1; $step <= $move['steps']; $step++) {

                $headCoordinates = $this->getNewCoordinates($move['direction'], $headCoordinates);

                $knotsPool[0] = $headCoordinates;

                if (count($knotsPool) != $knotsAmount) {
                    for ($i = 1; $i < $knotsAmount; $i++) {
                        $knotsPool[] = $startCoordinates;
                    }
                }

                foreach ($knotsPool as $k => $knotCoordinates) {
                    if (!isset($knotsPool[$k + 1])) {
                        continue;
                    }

                    $headC = $knotsPool[$k];
                    $tailC = $knotsPool[$k + 1];

                    $newKnotCoordinates = $this->moveTail($tailC, $headC);

                    $knotsPool[$k + 1] = $newKnotCoordinates;

                    $partOne[$newKnotCoordinates['x'] . ' - ' . $newKnotCoordinates['y']] = '#';

                    if ($k === 8) {
                        $partTwo[$newKnotCoordinates['x'] . ' - ' . $newKnotCoordinates['y']] = '#';
                    }
                }
            }
        }

        dump('Part One:' . count($partOne));
        dd('Part Two:' . count($partTwo));

    }

    protected function getNewCoordinates($direction, array $coordinates): array
    {
        if ($direction === 'R') {
            $coordinates['x']++;
        } elseif ($direction === 'L') {
            $coordinates['x']--;
        } elseif ($direction === 'U') {
            $coordinates['y']++;
        } elseif ($direction === 'D') {
            $coordinates['y']--;
        }

        return $coordinates;
    }

    protected function moveTail(array $tailCoordinates, array $headCoordinates)
    {
        $headX = $headCoordinates['x'];
        $headY = $headCoordinates['y'];

        $tailX = $tailCoordinates['x'];
        $tailY = $tailCoordinates['y'];

        if ($headX === $tailX && $headY === $tailY) {
            // Head covers Tail
        } elseif ($headY === $tailY && ($tailX+1) === $headX) {
            // TH
        } elseif ($headY === $tailY && ($tailX-1) === $headX) {
            // HT
        } elseif ($headY === $tailY && ($tailX+2) === $headX) {
            // T.H
            $tailCoordinates['x']++;
        } elseif ($headY === $tailY && ($tailX-2) === $headX) {
            // H.T
            $tailCoordinates['x']--;
        } elseif ($headX === $tailX && ($tailY+1) === $headY) {
            // ..H..
            // ..T..
        } elseif ($headX === $tailX && ($tailY-1) === $headY) {
            // ..T..
            // ..H..
