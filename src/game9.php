        } elseif ($tailY - 1 === $headY && $tailX - 2 === $headX) {
            // ..T.
            // H...
            $tailCoordinates['x']--;
            $tailCoordinates['y']--;
        } elseif ($tailY + 2 === $headY && $tailX + 2 === $headX) {
            // ..H..
            // .....
            // T....
            $tailCoordinates['x']++;
            $tailCoordinates['y']++;
