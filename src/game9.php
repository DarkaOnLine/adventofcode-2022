            // .H...
            // .....
            // ..T..
            $tailCoordinates['x']--;
            $tailCoordinates['y']++;
        } elseif ($tailY + 2 === $headY && $tailX + 1 === $headX) {
            // ...H.
            // .....
            // ..T..
            $tailCoordinates['x']++;
            $tailCoordinates['y']++;
        } elseif ($headX === $tailX && ($tailY-2) === $headY) {
            // ..T..
            // .....
            // ..H..
            $tailCoordinates['y']--;
        } elseif ($tailY - 2 === $headY && $tailX + 1 === $headX) {
            // ..T..
            // .....
            // ...H.
            $tailCoordinates['y']--;
            $tailCoordinates['x']++;
        } elseif ($tailY - 2 === $headY && $tailX - 1 === $headX) {
            // ..T..
            // .....
            // .H...
            $tailCoordinates['x']--;
            $tailCoordinates['y']--;
        } elseif ($tailY + 1 === $headY && $tailX + 1 === $headX) {
            // ....H
            // ...T.
        } elseif ($tailY + 1 === $headY && $tailX - 1 === $headX) {
            // ...H..
            // ....T.
        } elseif ($tailY - 1 === $headY && $tailX - 1 === $headX) {
            // ....T
            // ...H.
        } elseif ($tailY - 1 === $headY && $tailX + 1 === $headX) {
            // ...T..
            // ....H.
